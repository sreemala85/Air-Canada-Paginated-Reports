				, 99.0 AS REGION_ORDERX
				, MAIN_LVL AS NEW_FIELD_LVL1
				, 9 AS LVL1
				, ''Y'' AS ORDERX_LVL1
				, REPOT_LVL AS NEW_FIELD_LVL2
