		),
		-- LVL3 query to aggregate and compute values
		CTE_LVL3 AS (
			SELECT
				SUM(RL_10) AS "10"
				, SUM(RL_20) AS "20"
				, SUM(RL_21) AS "21"
				, SUM(RL_26) AS "26"
				, SUM(RL_30) AS "30"
				, SUM(RL_40) AS "40"
				, SUM(RL_50) AS "50"
				, SUM(RL_53) AS "53"
				, SUM(RL_54) AS "54"
				, SUM(RL_57) AS "57"
				, SUM(RL_60) AS "60"
				, SUM(RL_70) AS "70"
				, SUM(RL_77) AS "77"
				, SUM(RL_ERROR) AS RL_ERROR
				, SUM(PAX) AS PAX
				, SUM(FILES) AS FILES
