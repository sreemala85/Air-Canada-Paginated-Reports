		),
		-- LVL2 query to aggregate and compute values
		CTE_LVL2 AS (
			SELECT
				SUM(RL_10) AS "10"
				, SUM(RL_20) AS "20"
				, SUM(RL_21) AS "21"
				, SUM(RL_26) AS "26"
