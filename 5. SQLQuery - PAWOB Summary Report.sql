				, LVL3 AS LVL
				, ORDERX_LVL3 AS ORDERX
				, REGION_ORDER
			FROM
				CTE_SITA_MASTER_REPORT
			GROUP BY
				NEW_FIELD
				, LVL
				, ORDERX
				, REGION_ORDER
		),
		-- Union query to combine all data
		CTE_UNION AS (
			SELECT * FROM CTE_LVL0
			UNION
			SELECT * FROM CTE_LVL1
			UNION
			SELECT * FROM CTE_LVL2
			UNION
			SELECT * FROM CTE_LVL3
		)
		-- Summarize all combined data
		SELECT
			SUM("10") AS "10"
			, SUM("20") AS "20"
			, SUM("21") AS "21"
			, SUM("26") AS "26"
			, SUM("30") AS "30"
			, SUM("40") AS "40"
			, SUM("50") AS "50"
			, SUM("53") AS "53"
			, SUM("54") AS "54"
			, SUM("57") AS "57"
			, SUM("60") AS "60"
			, SUM("70") AS "70"
			, SUM("77") AS "77"
			, SUM(RL_ERROR) AS RL_ERROR
			, SUM(PAX) AS PAX
			, SUM(FILES) AS FILES
			, SUM(RATIO) AS RATIO
			, SUM(TARGET) AS TARGET
			, NEW_FIELD
			, LVL
			, ORDERX
			, REGION_ORDER
		FROM
			CTE_UNION
		GROUP BY
			NEW_FIELD
			, LVL
			, ORDERX
			, REGION_ORDER
		ORDER BY
			NEW_FIELD
			, LVL
			, ORDERX
			, REGION_ORDER;

