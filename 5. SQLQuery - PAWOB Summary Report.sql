					WHEN SORT_ORDER = 6 THEN ''F''
					WHEN SORT_ORDER = 7 THEN ''G''
					WHEN SORT_ORDER = 8 THEN ''H''
					WHEN SORT_ORDER = 9 THEN ''I''
					WHEN SORT_ORDER = 10 THEN ''J''
					ELSE NULL
				END AS ORDERX_LVL3
				, RL_10
				, RL_20
				, RL_21
				, RL_26
				, RL_30
				, RL_40
				, RL_50
				, RL_53
				, RL_54
				, RL_57
				, RL_60
				, RL_70
				, RL_77
				, RL_ERROR
				, PAX
				, TARGET_FLAG
				, SYSTEM_TARGET
				, GLOBAL_TARGET
				, REGIONAL_TARGET
				, STATION_TARGET
				, REGION_ORDER
			FROM
				BI_REPORTS_OPS.TBL_PRST_CMN_SITA_MASTER_REPORT
			WHERE
				TARGET_FLAG = 1
					-- AND DATE_FLIGHT >= TO_CHAR(P_START_DT)
					-- AND DATE_FLIGHT <= TO_CHAR(P_END_DT)
                AND DATE_FLIGHT BETWEEN P_START_DATE AND P_END_DATE
		),
		-- LVL0 query to aggregate and compute values
		CTE_LVL0 AS (
			SELECT
				SUM(RL_10) AS "10"
				, SUM(RL_20) AS "20"
				, SUM(RL_21) AS "21"
				, SUM(RL_26) AS "26"
				, SUM(RL_30) AS "30"
				, SUM(RL_40) AS "40"
